package com.ch.pojo;

public class WebSite {
	private int id;
	private String crawerurl;
	private String preurl;
	private String urlrule;
	private String titlerule;
	private String hitsrule;
	private String commentsrule;
	private String publishtimerule;
	private int pagenum;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCrawerurl() {
		return crawerurl;
	}
	public void setCrawerurl(String crawerurl) {
		this.crawerurl = crawerurl;
	}
	public String getPreurl() {
		return preurl;
	}
	public void setPreurl(String preurl) {
		this.preurl = preurl;
	}
	public String getUrlrule() {
		return urlrule;
	}
	public void setUrlrule(String urlrule) {
		this.urlrule = urlrule;
	}
	public String getTitlerule() {
		return titlerule;
	}
	public void setTitlerule(String titlerule) {
		this.titlerule = titlerule;
	}
	public String getHitsrule() {
		return hitsrule;
	}
	public void setHitsrule(String hitsrule) {
		this.hitsrule = hitsrule;
	}
	public String getCommentsrule() {
		return commentsrule;
	}
	public void setCommentsrule(String commentsrule) {
		this.commentsrule = commentsrule;
	}
	public String getPublishtimerule() {
		return publishtimerule;
	}
	public void setPublishtimerule(String publishtimerule) {
		this.publishtimerule = publishtimerule;
	}
	public int getPagenum() {
		return pagenum;
	}
	public void setPagenum(int pagenum) {
		this.pagenum = pagenum;
	}
	
	
	
}
